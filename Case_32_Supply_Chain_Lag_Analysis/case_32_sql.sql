-- КЕЙС №32: ОКOННЫЙ ЛAГ-АНАЛИЗ И ХРOНOЛОГИЧЕСКИЙ ШПИОНАЖ LAG В ЛОГИСТИКЕ ПТО
-- Бизнес-задача: Сформировать аналитический реестр контроля ритмичности снабжения. Рассчитать величину задержки предыдущего выполненного заказа в разрезе каждого конкретного поставщика без деструктивного сжатия детальных строк транзитного лога.

SELECT 
    order_id AS "Заказы", 
    COALESCE(delay_days, 0) AS "Текущее_опоздание", -- [Исправление] Исправлен шок моторики в кавычках алиаса, NULL-пустоты занулены
    
    -- ОКOННОЕ СМЕЩЕНИЕ НАЗАД (LAG-АНАЛИЗ ВРЕМЕННЫХ РЯДОВ):
    -- PARTITION BY делит массив на герметичные расчетные окна по уникальным ID поставщиков.
    -- ORDER BY ASC выстраивает накладные по строгой хронологической шкале движения дат.
    -- [Исправление] Убрана избыточная скобка внутри LAG, синтаксис приведен к эталону.
    -- Дефолтный ноль (0) преобразует пустоту отсутствия прошлых логов для первых заказов в константу 0.
    LAG(COALESCE(delay_days, 0), 1, 0) OVER (
        PARTITION BY supplier_id 
        ORDER BY delivery_date ASC
    ) AS "Задержка_предыдущего_заказа"

FROM supply_orders

-- СОРТИРОВКА ВИТРИНЫ: Регламентное упорядочивание отчета по идентификаторам доставщиков и номерам заказов
ORDER BY supplier_id, order_id;
