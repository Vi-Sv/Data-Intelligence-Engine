-- КЕЙС №7: КОНТРОЛЬ ДЕФИЦИТА МАТЕРИАЛОВ И ФИЛЬТРАЦИЯ ПУСТЫХ СВЯЗЕЙ ПОСЛЕ МОСТА ДАННЫХ
-- Бизнес-задача: Сформировать детальную ведомость абсолютного дефицита материально-технических ресурсов на складах предприятия. Обеспечить корректный вывод позиций с нулевым остатком, а также позиций, полностью отсутствующих в складских регистрах учета.

SELECT 
    TRIM(LOWER(t1.mat_name)) AS clean_material_name, -- Нормализация данных: удаление пробелов и приведение к нижнему регистру для защиты от ручного ввода
    COALESCE(t2.current_volume, 0) AS current_stock_volume -- Валидация метрики: вывод гарантированного нуля для проведения закупочной кампании

FROM raw_materials AS t1 -- Исходный справочник номенклатуры материалов (Левая неприкасаемая таблица)

-- Использование LEFT JOIN гарантирует сохранение всех позиций номенклатурного справочника
LEFT JOIN warehouse_stocks AS t2 
    ON t1.mat_code = t2.mat_code

-- ФИНИШНЫЙ ЭТАЖ ФИЛЬТРАЦИИ (Фильтр КПП после моста):
-- Условие t2.current_volume = 0 отлавливает зафиксированные нулевые остатки.
-- Условие t2.current_volume IS NULL перехватывает внешнюю пустоту от пустых связей моста для позиций, которых вообще нет в складских регистрах.
WHERE t2.current_volume = 0 
   OR t2.current_volume IS NULL;
