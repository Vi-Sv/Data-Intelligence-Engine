-- Бизнес-задача: Выявить сотрудников, чей индивидуальный оклад строго превышает среднюю заработную плату их структурного подразделения. Реализовать предварительное агрегирование ФОТ на нижнем уровне и сквозное сопряжение данных.

WITH dept_averages AS (
    -- ВЫЧИСЛИТЕЛЬНЫЙ ЦЕХ (CTE): Расчет средних окладов по подразделениям холдинга
    SELECT 
        TRIM(LOWER(department_name)) AS g1, -- Нормализация текстового ввода: удаление пробелов и приведение к единому регистру
        -- [Исправление] Типизация ::numeric и округление корректно инкапсулированы внутрь скобок функции ROUND()
        ROUND(AVG(COALESCE(salary, 0))::NUMERIC, 2) AS g2
    FROM employee_salaries
    GROUP BY TRIM(LOWER(department_name))   -- Группировка для формирования уникальных ключей департаментов
)

-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Сопряжение детального лога со сжатой CTE-матрицей средних показателей
SELECT 
    f1.emp_id AS "ID_сотрудника", 
    TRIM(LOWER(f1.department_name)) AS "Департамент", -- [Исправление] Префикс f1 перенесен непосредственно к физическому полю
    COALESCE(f1.salary, 0) AS "Оклад",                 -- [Исправление] Префикс f1 перенесен непосредственно к физическому полю
    f2.g2 AS "Средний_оклад_по_департаменту"

FROM employee_salaries AS f1

-- ВНУТРЕННЕЕ СОПРЯЖЕНИЕ (Мост один-ко-многим): Пришиваем сжатые метрики по ключу совпадения наименований отделов
INNER JOIN dept_averages AS f2
    ON TRIM(LOWER(f1.department_name)) = f2.g1

-- ХИРУРГИЧЕСКОЕ СИТО WHERE: Локализация персонала с окладом строго выше среднего норматива по цеху
WHERE COALESCE(f1.salary, 0) > f2.g2 

-- СОРТИРОВКА ВИТРИНЫ: Ранжирование вывода по убыванию величины очищенного оклада
ORDER BY "Оклад" DESC; -- [Исправление] Прямой вызов чистого алиаса вывода устраняет риск коллизии адресации
