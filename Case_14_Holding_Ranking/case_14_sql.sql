-- КЕЙС №14: ОКОННОЕ РАНЖИРОВАНИЕ ROW_NUMBER И ВЫРЕЗАНИЕ ТОП-3 КРУПНЫХ ПЛАТЕЖЕЙ
-- Бизнес-задача: Выполнить многомерный аналитический срез коммерческой эффективности регионов. Реализовать ранжирование транзакций внутри географических узлов и изолировать топ-3 крупнейших платежей по каждому региону.

SELECT 
    TAB2.clean_region AS "Регионы",
    TAB2.client_id AS "ID_клиента",
    TAB2.clean_amount AS "Сумма_платежа",
    TAB2.row_rank AS "Рейтинг"

FROM (
    -- ВНУТРЕННИЙ СЛОЙ («МАТРЕШКА»): Секционирование и развертывание рангового автомата
    SELECT 
        TRIM(LOWER(region_name)) AS clean_region, -- Нормализация данных: удаление пробелов и приведение к единому регистру
        client_id AS client_id,                   -- Идентификатор контрагента
        COALESCE(pay_amount, 0) AS clean_amount,  -- Валидация метрики: замена NULL на 0 для защиты расчетов
        
        -- АНАЛИТИЧЕСКОЕ РАНЖИРОВАНИЕ: Нумератор ROW_NUMBER() присваивает сквозные порядковые номера строкам.
        -- PARTITION BY изолирует данные внутри расчетных перегородок каждого региона.
        -- ORDER BY DESC сортирует транзакции от максимальных сумм к минимальным для выделения лидеров.
        ROW_NUMBER() OVER (
            PARTITION BY TRIM(LOWER(region_name)) 
            ORDER BY COALESCE(pay_amount, 0) DESC
        ) AS row_rank
    FROM regional_payments
) AS TAB2

-- ХИРУРГИЧЕСКОЕ СИТО WHERE (КПП верхнего уровня):
-- Вырезаем строго топ-3 транзакции по каждому региону. Все строки ниже 3-го места отсекаются.
WHERE TAB2.row_rank IN (1, 2, 3)

-- СОРТИРОВКА ВИТРИНЫ: По алфавиту регионов, а внутри них — по возрастанию рейтинга (от 1 до 3)
ORDER BY 1 ASC, 4 ASC;
