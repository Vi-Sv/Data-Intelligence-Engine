-- КЕЙС №31: СВOДНЫЕ МAТPИЦЫ И ГОРИЗОНТАЛЬНЫЙ РАЗВОРOТ PIVOT В ФИНТЕХ-КУБЕ
-- Бизнес-задача: Спроектировать высокопроизводительную коммерческую витрину поквартальной динамики продаж. Переформатировать вертикальный массив логов в горизонтальную кросс-матрицу (Pivot Table) с распределением выручки по четырем финансовым кварталам за один проход вычислительного ядра СУБД.

SELECT 
    TRIM(LOWER(product_line)) AS "Категория_продукта",
    
    -- ГОРИЗОНТАЛЬНЫЙ РАЗВОРOT ВЫРУЧКИ ПО КВАРТАЛАМ (MANUAL PIVOT):
    -- Логические переключатели CASE WHEN имплантированы внутрь агрегатных сумматоров SUM().
    -- Движок проверяет консистентность квартальных масок ('Q1', 'Q2', 'Q3', 'Q4') без LOWER-очистки,
    -- строго соответствуя исходному регистру физического поля quarter_name.
    -- Скалярные предохранители COALESCE зануляют системные пустоты NULL в объемах доходов.
    SUM(CASE WHEN quarter_name = 'Q1' THEN COALESCE(revenue_amount, 0) ELSE 0 END) AS "Выручка_Q1",
    SUM(CASE WHEN quarter_name = 'Q2' THEN COALESCE(revenue_amount, 0) ELSE 0 END) AS "Выручка_Q2",
    SUM(CASE WHEN quarter_name = 'Q3' THEN COALESCE(revenue_amount, 0) ELSE 0 END) AS "Выручка_Q3",
    SUM(CASE WHEN quarter_name = 'Q4' THEN COALESCE(revenue_amount, 0) ELSE 0 END) AS "Выручка_Q4"

FROM store_sales -- [Верификация] Исходный физический адрес таблицы успешно восстановлен Хостом

-- МАКРО-ГРУППИРОВКА: Схлопывание строк лога по уникальным кластерам нормализованных продуктовых линеек
GROUP BY TRIM(LOWER(product_line))

-- СОРТИРОВКА ВИТРИНЫ: Упорядочивание кросс-реестра по алфавитному признаку категорий товаров
ORDER BY 1 ASC;
