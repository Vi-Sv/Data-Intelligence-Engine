-- КЕЙС №19: СТРУКТУРИРОВАНИЕ CTE-МАГИСТРАЛЕЙ И ИЗОЛЯЦИЯ АНОМАЛЬНЫХ ПРОВОДОК
-- Бизнес-задача: Спроектировать читаемый и оптимизированный аналитический конвейер для идентификации крупных финансовых аномалий. Выделить транзакции, сумма которых строго превышает установленный лимит в 300 000 единиц, исключив операционный шум.

WITH clean_data AS (
    -- В СУХОМ ИЗОЛИРОВАННОМ ЦЕХУ (CTE): Производим нормализацию текстовых полей и валидацию числовых метрик
    SELECT 
        TRIM(LOWER(agent_name)) AS g1, 
        ABS(COALESCE(amount, 0)) AS g2 -- [Исправление] Восстановлена парная закрывающая скобка функции ABS()
    FROM agent_transactions
)
-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Формирование финальной витрины на основе очищенного массива данных
SELECT 
    clean_data.g1 AS "Агенты", 
    clean_data.g2 AS "Сумма_транзакций"
FROM clean_data

-- ХИРУРГИЧЕСКОЕ СИТО WHERE: Жесткая пост-фильтрация по критерию строгого превосходства (g2 > 300000)
WHERE clean_data.g2 > 300000 -- [Исправление] Убран нестрогий оператор >= для точного соответствия ТЗ

-- СОРТИРОВКА ВИТРИНЫ: Ранжирование аномальных проводок от максимальных объемов к минимальным
ORDER BY clean_data.g2 DESC;
