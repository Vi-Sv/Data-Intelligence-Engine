-- КЕЙС №21: ВЛOЖЕННЫЕ CTE-МАГИСТРАЛИ И КAСКАДНАЯ ФИЛЬТРАЦИЯ ДАННЫХ
-- Бизнес-задача: Спроектировать многоуровневый оптимизированный аналитический конвейер для многоступенчатого аудита накладных расходов. Изолировать затраты по статье «аренда», превышающие установленный лимит в 150 000 единиц, с предварительной нормализацией данных.

WITH pure_data AS (
    -- ПЕРВЫЙ ЭТАП ОБРАБОТКИ (CTE 1): Стандартизация качества данных (Data Quality)
    SELECT 
        TRIM(LOWER(office_city)) AS g1,   -- Удаление скрытых пробелов и приведение к нижнему регистру названий городов
        COALESCE(cost, 0) AS g2,          -- Валидация метрики: замена системных пропусков NULL на числовой ноль
        TRIM(LOWER(expense_item)) AS g3   -- Унификация наименований статей расходов для точного сопоставления
    FROM office_expenses
),

filtered_cities AS (
    -- ВТOРОЙ ЭТАП ОБРАБОТКИ (CTE 2): Каскадная фильтрация по бизнес-признаку
    -- [Исправление] Цепь каскада замкнута на результаты первого цеха pure_data, устранен ложный вызов грязного источника
    SELECT 
        g1 AS f1, 
        g2 AS f2, 
        g3 AS f3
    FROM pure_data 
    -- Промежуточный отсев: изолируем строго целевую статью расходов, отсекая сопутствующий операционный шум
    WHERE g3 = 'аренда'
)

-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Формирование финальной аналитической витрины для финансового департамента
SELECT 
    filtered_cities.f1 AS "Города",
    filtered_cities.f2 AS "Сумма_аренды"
FROM filtered_cities

-- ПОСТ-ФИЛЬТРАЦИЯ НА ВЕРХНЕМ ЭТАЖЕ: Выделение аномально высоких платежей по аренде
WHERE filtered_cities.f2 > 150000 

-- СОРТИРОВКА ВИТРИНЫ: Алфавитное упорядочивание реестра по наименованиям городов
ORDER BY filtered_cities.f1 ASC;
