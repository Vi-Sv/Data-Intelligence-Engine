-- Бизнес-задача: Выделить высокодоходный VIP-сегмент клиентов по объему фактически оплаченных заказов с порогом вхождения от 500 000 рублей.

SELECT 
    TRIM(LOWER(client_name)) AS clean_client_name, -- 1. Нормализация данных: выжигаем скрытые пробелы и приводим к единому регистру
    SUM(COALESCE(payment_amount, 0)) AS clean_sum  -- 2. Валидация: COALESCE заменяет NULL нулями, защищая математику агрегатора SUM

FROM raw_orders -- Входной поток сырых коммерческих логов

WHERE status = 'Paid' -- НА ПЕРВОМ ЭТАЖЕ: Срезаем мусор неоплаченных или отмененных строк

GROUP BY TRIM(LOWER(client_name)) -- НА ВТОРОМ ЭТАЖЕ: Схлопываем данные по очищенным именам клиентов

HAVING SUM(COALESCE(payment_amount, 0)) > 500000 -- СИТО: Взвешиваем готовые суммы и отсекаем всех, кто ниже VIP-порога

ORDER BY clean_sum DESC; -- СОРТИРОВКА ВИТРИНЫ: Выводим гигантов оборота по убыванию
