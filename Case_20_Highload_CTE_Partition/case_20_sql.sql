-- КЕЙС №20: МНОГОСТУПЕНЧАТЫЕ CTE-МАГИСТРАЛИ И СЕКЦИОНИРОВАНИЕ ТОП-2 ПЕРЕВОДОВ ХОЛДИНГА
-- Бизнес-задача: Спроектировать высокопроизводительный аналитический конвейер для сегментации межбанковских переводов. Реализовать локальное ранжирование транзакций внутри каждого филиала и изолировать строго ТОП-2 крупнейших переводов по каждому городу холдинга.

WITH cleansed_branches AS (
    -- В ИЗОЛИРОВАННОМ СТЕРИЛЬНОМ ЦЕХУ (CTE): Выполняем предварительный аудит, очистку и секционированное ранжирование
    SELECT 
        TRIM(LOWER(branch_code)) AS g1,  -- Нормализация текстового ввода: удаление концевых пробелов и приведение к нижнему регистру
        COALESCE(transfer_amount, 0) AS g2, -- Валидация метрики: замена системных пустот NULL числовым нулем
        
        -- АНАЛИТИЧЕСКОЕ РАНЖИРОВАНИЕ: Нумератор ROW_NUMBER() штампует порядковые номера строк.
        -- PARTITION BY нарезает таблицу на герметичные изолированные окна по чистым названиям городов.
        -- ORDER BY DESC выстраивает объемы переводов по убыванию для локализации финансовых лидеров.
        ROW_NUMBER() OVER (
            PARTITION BY TRIM(LOWER(branch_code)) 
            ORDER BY COALESCE(transfer_amount, 0) DESC
        ) AS g3
    FROM branch_transfers -- [Исправление] Восстановлен утерянный адрес исходной физической таблицы СУБД
)
-- ГЛАВНЫЙ ВНЕШНИЙ КОНТУР: Формирование финальной VIP-витрины для финансового директора
SELECT 
    cleansed_branches.g1 AS "Города", 
    cleansed_branches.g2 AS "Сумма", 
    cleansed_branches.g3 AS "Ранг"
FROM cleansed_branches

-- ХИРУРГИЧЕСКОЕ СИТО WHERE: Вырезаем строго золото и серебро (ранги 1 и 2). Весь последующий массив отсекается.
WHERE cleansed_branches.g3 <= 2

-- СОРТИРОВКА ВИТРИНЫ: По алфавитному порядку городов и естественному течению мест от лидеров вниз (1, 2)
ORDER BY cleansed_branches.g1 ASC, cleansed_branches.g3 ASC; -- [Исправление] Вторичный вектор выровнен на ASC
