-- КЕЙС №5: АНАЛИЗ И СЕГМЕНТАЦИЯ ПРОИЗВОДСТВЕННОГО БРАКА ПО ЛИНИЯМ (БЛОК: ОТК)
-- Бизнес-задача: Локализовать производственные линии с критическим объемом брака (более 15 000 единиц) на основе верифицированных комментариев службы ОТК.

SELECT 
    TRIM(CAST(line_id AS VARCHAR)) AS clean_line_id, -- 1. Нормализация ID линий: удаление невидимых концевых пробелов
    SUM(COALESCE(defect_count, 0)) AS total_defect_count -- 2. Валидация метрики: замена NULL на 0 для корректного суммирования

FROM raw_manufacture -- Исходный лог производственного департамента

-- ПЕРВИЧНАЯ ФИЛЬТРАЦИЯ: Приведение грязного текстового ввода ОТК к единому регистру и удаление пробелов перед валидацией
WHERE TRIM(LOWER(inspector_comment)) = 'брак'

GROUP BY TRIM(CAST(line_id AS VARCHAR)) -- Группировка по нормализованным идентификаторам линий

-- ПОСТ-ФИЛЬТРАЦИЯ АГРЕГАТОВ: Отсечение производственных узлов, не превысивших критический порог ЧП
HAVING SUM(COALESCE(defect_count, 0)) > 15000;
