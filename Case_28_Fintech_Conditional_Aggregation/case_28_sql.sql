-- КЕЙС №28: ЛOГИЧЕСКOЕ ВEТВЛEНИЕ CASE WHEN ВНУТРИ АГРEГAЦИИ ДЛЯ СБОРКИ PIVOT-ВИТРИНЫ
-- Бизнес-задача: Сформировать многомерную коммерческую витрину эффективности менеджеров. Рассчитать общий макро-оборот по каждому сотруднику с одновременной изоляцией продаж по целевым категориям («электроника» и «одежда») за один проход вычислительного ядра СУБД.

SELECT 
    TRIM(LOWER(manager_name)) AS "Менеджер",
    SUM(COALESCE(amount, 0)) AS "Общая_сумма_всех_продаж_менеджера", -- [Исправление] Коррекция орфографии моторики пальцев
    
    -- УСЛОВНАЯ АГРЕГАЦИЯ КАТЕГОРИИ ЭЛЕКТРОНИКА:
    -- Логический переключатель CASE WHEN имплантирован внутрь агрегатора SUM().
    -- [Исправление] Строковая маска переведена в нижний регистр 'электроника' под стать функции LOWER() для исключения логического сбоя.
    -- Кириллический алиас упакован в канонические двойные кавычки "" для защиты кодировок.
    SUM(
        CASE
            WHEN TRIM(LOWER(item_category)) = 'электроника' THEN COALESCE(amount, 0)
            ELSE 0
        END
    ) AS "Сумма_продаж_электроники", 

    -- УСЛОВНАЯ АГРЕГАЦИЯ КАТЕГОРИИ ОДЕЖДА:
    -- [Исправление] Строковая маска приведена к нижнему регистру 'одежда' для обеспечения консистентности Data Quality.
    SUM(
        CASE
            WHEN TRIM(LOWER(item_category)) = 'одежда' THEN COALESCE(amount, 0)
            ELSE 0
        END
    ) AS "Сумма_продаж_одежды"

FROM store_transactions

-- ПЕРВИЧНЫЙ ОТСЕВ: Сбриваем технический и отладочный мусор по маске строки до этапа группировки
WHERE TRIM(LOWER(manager_name)) NOT LIKE '%тест%'

-- МАКРО-ГРУППИРОВКА: Схлопывание строк по уникальным кластерам нормализованных имен менеджеров
GROUP BY TRIM(LOWER(manager_name))

-- СОРТИРОВКА ВИТРИНЫ: Ранжирование менеджеров по общему макро-обороту от максимального к минимальному
ORDER BY SUM(COALESCE(amount, 0)) DESC;
