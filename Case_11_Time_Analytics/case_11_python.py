# 2. ПРИВЕДЕНИЕ ТИПОВ ДАННЫХ ВРЕМЕННОГО ПОТОКА
# Явно переводим строковые метки времени в формат datetime для точной хронологической сортировки
df['pay_date'] = pd.to_datetime(df['pay_date'])

# 3. СОРТИРОВКА ПОТОКА ПЕРЕД ОКРУЖЕНИЕМ СМЕЩЕНИЯ
# Сортируем датафрейм по клиентам и возрастанию дат (ОБЯЗАТЕЛЬНЫЙ шаг перед вызовом смещений)
df = df.sort_values(by=['client_id', 'pay_date'], ascending=[True, True]).copy()

# 4. ОКОННЫЙ КОНВЕЙЕР СМЕЩЕНИЯ (Аналог LAG() OVER(PARTITION BY ... ORDER BY))
# Группируем фрейм по client_id для локализации расчетов.
# Метод .shift(1) выполняет сдвиг вектора данных на одну строку вниз (шаг назад по времени).
# Заменяем образовавшиеся пустые значения NaN для первых строк на жесткий ноль 0.
df['previous_payment_amount'] = df.groupby('client_id')['amount'].shift(1).fillna(0).astype(int)

# 5. ФОРМИРОВАНИЕ И ОФОРМЛЕНИЕ ФИНАЛЬНОЙ ВИТРИНЫ БИЗНЕСА
final_view = df[['client_id', 'pay_date', 'amount', 'previous_payment_amount']]
final_view.columns = ['ID клиента', 'Дата текущего платежа', 'Сумма текущего платежа', 'Сумма предыдущего платежа']
